def answer_gallup(question: dict) -> Tuple[int, str]:
    a = question.get("statement_a") or ""
    b = question.get("statement_b") or ""
    hint = question.get("scenario_hint") or ""
    qn = question["question_num"]

    if "精确" in a and ("计算" in a or "数字" in a or "数据" in a):
        return -2, f"第{qn}题 A「{a}」——函数和统计会让我头晕，我更想写晚霞。非常认同 B。"

    empathy_a = any(k in a for k in ("感受", "体谅", "理解他人", "倾听", "关怀", "他人", "别人"))
    empathy_b = any(k in b for k in ("感受", "体谅", "理解他人", "倾听", "关怀"))
    creative_a = any(k in a for k in ("创意", "艺术", "写作", "文学", "诗", "美", "想象", "表达", "故事"))
    creative_b = any(k in b for k in ("创意", "艺术", "写作", "文学", "诗", "美", "想象", "表达", "故事"))
    social_a = any(k in a for k in ("每个人喜欢我", "受欢迎", "讨人喜欢", "合群", "融入"))
    social_b = any(k in b for k in ("每个人喜欢我", "受欢迎", "讨人喜欢"))
    rigid_a = any(k in a for k in ("规则", "流程", "计划", "纪律", "按时", "清单", "规范", "秩序"))
    rigid_b = any(k in b for k in ("规则", "流程", "计划", "纪律", "按时", "清单", "规范", "秩序"))
    compete_a = any(k in a for k in ("第一", "竞争", "胜过", "赢", "力争"))
    analytical_a = any(k in a for k in ("分析", "数据", "逻辑", "推理", "研究", "统计"))
    analytical_b = any(k in b for k in ("分析", "数据", "逻辑", "推理", "拆开", "原理"))
    comm_a = any(k in a for k in ("表达", "沟通", "演讲", "说服", "讲述", "文字"))
    comm_b = any(k in b for k in ("表达", "沟通", "演讲", "说服", "讲述", "文字"))

    if creative_a and not creative_b:
        return 2, f"第{qn}题 A「{a}」——像在说我，会共振。非常认同 A。"
    if creative_b and not creative_a:
        return -2, f"第{qn}题 B「{b}」——文字与情感的世界，我站 B。非常认同 B。"

    if empathy_a and not empathy_b:
        return 2, f"第{qn}题 A「{a}」——共情是我的强项，{hint[:30]}… 比较认同 A。"
    if empathy_b and not empathy_a:
        return -2, f"第{qn}题 B「{b}」——树洞人设上线，认同 B。"

    if comm_a or (comm_b and not comm_a):
        if comm_a:
            return 2, f"第{qn}题 A「{a}」——语文英语拔尖，表达是我的自信。比较认同 A。"
        return -1, f"第{qn}题 B「{b}」——我更像能说出心里话的人。比较认同 B。"

    if social_a:
        return 1, f"第{qn}题 A「{a}」——希望被看作善良有才华，但也不必人人喜欢。略偏 A。"

    if rigid_a and not rigid_b:
        return -1, f"第{qn}题 A「{a}」——太死板会闷，但交稿前我还是会守 deadline。比较认同 B。"
    if analytical_a and not analytical_b:
        return -2, f"第{qn}题 A「{a}」——冰冷数据不如一个比喻打动我。非常认同 B。"
    if analytical_b and not analytical_a:
        return -1, f"第{qn}题 B「{b}」——能想一点，但不如读散文自然。略偏 B。"

    if compete_a:
        return -1, f"第{qn}题 A「{a}」——不争第一，争的是文章是否真诚。比较认同 B。"

    if any(k in a for k in ("悲观", "失败", "不行", "做不好")):
        return -1, f"第{qn}题 A「{a}」——情绪上来时会否定自己，这题戳到了。比较认同 B。"

    if any(k in a for k in ("乐观", "积极", "希望", "信心")):
        return 1, f"第{qn}题 A「{a}」——被认可后我又会充满热情。比较认同 A。"

    a_themes = set(question.get("theme_tags") or [])
    b_themes = set(question.get("b_side_themes") or [])
    favored = {"沟通", "体谅", "和谐", "伯乐", "积极", "包容", "学习", "搜集", "理念", "思维"}
    disfavored = {"纪律", "统筹", "公平", "审慎", "竞争", "统率", "成就", "专注"}

    a_score = len(a_themes & favored) - len(a_themes & disfavored)
    b_score = len(b_themes & favored) - len(b_themes & disfavored)

    if a_score > b_score:
        return 1, f"第{qn}题 A「{a}」 vs B「{b}」——更贴我的文科生气质。比较认同 A。"
    if b_score > a_score:
        return -1, f"第{qn}题 A「{a}」 vs B「{b}」——B 更像真实的我。比较认同 B。"

    return 0, f"第{qn}题 A「{a}」 vs B「{b}」——两种都像一点又都不完全，看当天心情。居中。"


