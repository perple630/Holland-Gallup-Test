def answer_gallup(question: dict) -> Tuple[int, str]:
    a = question.get("statement_a") or ""
    b = question.get("statement_b") or ""
    qn = question["question_num"]

    if any(k in a for k in ("独自", "一个人", "独处", "独立解决")):
        return -1, f"第{qn}题 A「{a}」——策划活动可以，一个人解数学题还是算了。有点不符合，认同 B。"
    if any(k in b for k in ("独自", "一个人", "独处")) and any(
        k in a for k in ("复杂", "分析", "数据", "逻辑", "研究")
    ):
        return -1, f"第{qn}题 A「{a}」 vs B「{b}」——我享受和人一起做事。比较认同 B。"

    people_a = any(k in a for k in ("他人", "别人", "团队", "合作", "沟通", "倾听", "体谅", "和谐", "信任", "关系"))
    people_b = any(k in b for k in ("他人", "别人", "团队", "合作", "沟通", "倾听", "体谅", "和谐", "信任"))
    lead_a = any(k in a for k in ("领导", "影响", "说服", "表达", "演讲", "号召", "组织", "推动"))
    lead_b = any(k in b for k in ("领导", "影响", "说服", "表达", "演讲", "号召", "组织"))
    abstract_a = any(k in a for k in ("分析", "数据", "逻辑", "理论", "研究", "抽象", "原理"))
    abstract_b = any(k in b for k in ("分析", "数据", "逻辑", "理论", "研究", "抽象"))
    positive_a = any(k in a for k in ("乐观", "积极", "热情", "信心", "希望", "开朗"))
    social_a = any(k in a for k in ("受欢迎", "讨人喜欢", "合群", "融入", "结识"))

    if people_a and not people_b:
        return 2, f"第{qn}题 A「{a}」——情商高、善于合作，这是我的自我画像。非常认同 A。"
    if people_b and not people_a:
        return -2, f"第{qn}题 B「{b}」——从人群获得能量，站 B。非常认同 B。"

    if lead_a and not lead_b:
        return 2, f"第{qn}题 A「{a}」——主持破冰、招新宣讲，我认 A。比较认同 A。"
    if lead_b and not lead_a:
        return -1, f"第{qn}题 B「{b}」——影响力是我的舒适区。比较认同 B。"

    if abstract_a and not abstract_b:
        return -1, f"第{qn}题 A「{a}」——数据分析与理论钻研偏弱，温和选低。比较认同 B。"
    if abstract_b and not abstract_a:
        return -1, f"第{qn}题 B「{b}」——抽象题不是我的强项。比较认同 B。"

    if positive_a:
        return 1, f"第{qn}题 A「{a}」——外向连接者，乐观务实，略偏 A。"

    if social_a:
        return 2, f"第{qn}题 A「{a}」——希望被看作情商高，但也会适度真诚。比较认同 A。"

    if any(k in a for k in ("弱点", "不足", "做不好", "不擅长")):
        return 1, f"第{qn}题 A「{a}」——适度暴露无害弱点以显真诚。有点认同 A。"

    a_themes = set(question.get("theme_tags") or [])
    b_themes = set(question.get("b_side_themes") or [])
    favored = {"沟通", "交往", "和谐", "伯乐", "体谅", "统率", "积极", "包容", "取悦", "自信", "行动"}
    disfavored = {"分析", "战略", "思维", "搜集", "专注", "审慎", "纪律", "公平"}

    a_score = len(a_themes & favored) - len(a_themes & disfavored)
    b_score = len(b_themes & favored) - len(b_themes & disfavored)

    if a_score > b_score:
        return 1, f"第{qn}题 A「{a}」 vs B「{b}」——更贴 HR 女生的外向连接气质。比较认同 A。"
    if b_score > a_score:
        return -1, f"第{qn}题 A「{a}」 vs B「{b}」——B 更像真实的我。比较认同 B。"

    return 0, f"第{qn}题 A「{a}」 vs B「{b}」——两种都有点像，选居中。"


