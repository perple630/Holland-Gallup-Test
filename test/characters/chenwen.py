def answer_gallup(question: dict) -> Tuple[int, str]:
    a = question.get("statement_a") or ""
    b = question.get("statement_b") or ""
    qn = question["question_num"]

    if any(k in a for k in ("突发", "应变", "意外", "混乱", "即兴", "冒险", "风险")):
        return -1, (
            f"第{qn}题 A「{a}」——我享受的是没有突发状况，每一次突发都是计划不周。比较认同 B。"
        )
    if any(k in a for k in ("计划", "流程", "按时", "秩序", "条理", "准确", "仔细", "复核", "规范", "清单")):
        return 1, f"第{qn}题 A「{a}」——有条不紊是我的舒适区，但不必选极端。比较认同 A。"
    if any(k in b for k in ("计划", "流程", "按时", "秩序", "条理", "准确", "仔细", "复核", "规范")):
        return -1, f"第{qn}题 B「{b}」——按规程做事让我安心。比较认同 B。"

    if any(k in a for k in ("负责", "可靠", "守信", "承诺", "完成", "交付", "坚持")):
        return 1, f"第{qn}题 A「{a}」——维持负责可靠的专业形象，比较认同 A。"
    if any(k in a for k in ("竞争", "赢", "第一", "胜过", "冒险", "大胆", "挑战")):
        return -1, f"第{qn}题 A「{a}」——本能想到风险清单，不太认同。比较认同 B。"

    ethics_a = any(k in a for k in ("诚实", "正直", "道德", "合规", "公平", "公正"))
    if ethics_a:
        return 2, f"第{qn}题 A「{a}」——职业道德题必须站稳，非常认同 A。"

    detail_a = any(k in a for k in ("细节", "错误", "核对", "检查", "精确", "数字", "数据"))
    detail_b = any(k in b for k in ("细节", "错误", "核对", "检查", "精确", "数字", "数据"))
    if detail_a and not detail_b:
        return 1, f"第{qn}题 A「{a}」——数字和细节是我的强项。比较认同 A。"
    if detail_b and not detail_a:
        return -1, f"第{qn}题 B「{b}」——复核习惯让我站 B。比较认同 B。"

    abstract_a = any(k in a for k in ("灵感", "创意", "空想", "哲学", "抽象", "天马行空"))
    if abstract_a:
        return -1, f"第{qn}题 A「{a}」——拉回具体怎么做、有没有先例。比较认同 B。"

    if any(k in a for k in ("受欢迎", "讨人喜欢", "焦点", "演讲", "表现")):
        return -1, f"第{qn}题 A「{a}」——公众演讲紧张，不爱出风头。比较认同 B。"

    help_a = any(k in a for k in ("帮助", "支持", "体贴", "体谅", "倾听", "他人"))
    if help_a:
        return 1, f"第{qn}题 A「{a}」——同事有事我会帮，但保持专业距离。比较认同 A。"

    a_themes = set(question.get("theme_tags") or [])
    b_themes = set(question.get("b_side_themes") or [])
    favored = {"成就", "纪律", "审慎", "责任", "专注", "公平", "排难", "分析", "回顾"}
    disfavored = {"追求", "理念", "行动", "竞争", "统率", "取悦", "前瞻", "自信"}

    a_score = len(a_themes & favored) - len(a_themes & disfavored)
    b_score = len(b_themes & favored) - len(b_themes & disfavored)

    if a_score > b_score:
        return 1, f"第{qn}题 A「{a}」 vs B「{b}」——务实结构化，略偏 A。比较认同 A。"
    if b_score > a_score:
        return -1, f"第{qn}题 A「{a}」 vs B「{b}」——有先例、可执行的一方更像我。比较认同 B。"

    return 0, f"第{qn}题 A「{a}」 vs B「{b}」——两种都不太极端，选居中。"


