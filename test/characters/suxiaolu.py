def answer_gallup(question: dict) -> Tuple[int, str]:
    """逐题阅读 A/B 陈述，以苏小鹿的直觉与情绪反应作答。"""
    a = question.get("statement_a") or ""
    b = question.get("statement_b") or ""
    hint = question.get("scenario_hint") or ""
    qn = question["question_num"]

    # ── 角色文档中的典型反应 ──
    if "满意" in a and ("工作" in a or "完成" in a):
        return -2, (
            f"第{qn}题 A「{a}」——开什么玩笑。我回看上周的设计稿简直想全部删掉。"
            f"满意？除非项目完全没有愚蠢的改稿。非常认同 B 侧。"
        )
    if "满意" in b:
        pass  # fall through

    # 完美主义 / 审美
    perfection_a = any(k in a for k in ("完美", "最好", "最高标准", "精益求精", "改进"))
    perfection_b = any(k in b for k in ("完美", "最好", "最高标准", "精益求精"))
    creative_a = any(k in a for k in ("创意", "艺术", "设计", "美", "想象", "独特", "新颖", "色彩", "表达"))
    creative_b = any(k in b for k in ("创意", "艺术", "设计", "美", "想象", "独特", "新颖", "色彩", "表达"))

    social_desirability_a = any(k in a for k in ("每个人喜欢我", "受欢迎", "讨人喜欢", "合群", "融入"))
    social_desirability_b = any(k in b for k in ("每个人喜欢我", "受欢迎", "讨人喜欢"))

    rigid_a = any(k in a for k in ("规则", "流程", "计划", "纪律", "按时", "清单", "规范", "秩序"))
    rigid_b = any(k in b for k in ("规则", "流程", "计划", "纪律", "按时", "清单", "规范", "秩序"))

    compete_a = any(k in a for k in ("第一", "竞争", "胜过", "赢", "力争"))
    compete_b = any(k in b for k in ("第一", "竞争", "胜过", "赢"))

    empathy_a = any(k in a for k in ("感受", "体谅", "理解他人", "倾听", "关怀"))
    empathy_b = any(k in b for k in ("感受", "体谅", "理解他人", "倾听"))

    future_a = any(k in a for k in ("未来", "远景", "趋势", "前瞻"))
    future_b = any(k in b for k in ("未来", "远景", "趋势", "历史", "过去"))

    analytical_a = any(k in a for k in ("分析", "数据", "逻辑", "推理", "研究"))
    analytical_b = any(k in b for k in ("分析", "数据", "逻辑", "推理", "拆开", "原理"))

    # 情感过载：价值观被戳中 → 极端
    if creative_a and not creative_b:
        return 2, f"第{qn}题 A「{a}」——这简直在说我。{hint[:40]}… 非常认同 A。"
    if creative_b and not creative_a:
        return -2, f"第{qn}题 B「{b}」——我的灵魂站 B。非常认同 B。"

    if perfection_a or ("质量" in a and "高" in a):
        return 2, f"第{qn}题 A「{a}」——我对作品永远不满意，但这题说的是追求品质，我认。非常认同 A。"

    if social_desirability_a:
        return -1, f"第{qn}题 A「{a}」——我不需要每个人都喜欢我，那样太假了。比较认同 B。"

    if rigid_a and not rigid_b:
        return -2, f"第{qn}题 A「{a}」——条条框框会扼死灵感，我选 B。非常认同 B。"
    if rigid_b and not rigid_a:
        return 2, f"第{qn}题 B「{b}」——自由对我太重要了，相对 A 我更不像 B… 等等，B 是 rigid？那选 A。"

    if compete_a and "参与" in b:
        return -1, f"第{qn}题：我不争第一，我在乎作品对不对味。比较认同 B「{b}」。"

    if empathy_a and empathy_b:
        return 1, f"第{qn}题：两个都有人情味，我会被触动，但不想装圣人。略偏 A。"

    if analytical_b and ("拆开" in b or "原理" in b):
        return -1, f"第{qn}题 B「{b}」——有点意思，但不如做 mood board 来得自然。略偏 B 或居中… 比较认同 B。"

    if future_a and "历史" in b:
        return 1, f"第{qn}题：我更想聊未来的视觉趋势，不太聊过去。比较认同 A。"

    # 自我否定模式（低 C / 拖延）
    if any(k in a for k in ("准时", "守时", "计划", "安排")):
        return -2, f"第{qn}题 A「{a}」——拖延是我的阴影，这题戳到痛处了。非常认同 B（或否定 A）。"

    if any(k in a for k in ("健谈", "主动", "陌生人", "结识")):
        return -1, f"第{qn}题 A「{a}」——我不是社牛，聚会久了就想躲。比较认同 B。"

    if any(k in b for k in ("健谈", "主动", "陌生人")):
        return 1, f"第{qn}题：内向如我，更不像 B 描述的那样。比较认同 A。"

    # 主题标签辅助（次优先级，仍带独白）
    a_themes = set(question.get("theme_tags") or [])
    b_themes = set(question.get("b_side_themes") or [])
    favored = {"完美", "理念", "个别", "沟通", "搜集", "积极", "前瞻", "思维"}
    disfavored = {"纪律", "专注", "统筹", "公平", "审慎", "责任", "排难", "竞争", "统率"}

    a_score = len(a_themes & favored) - len(a_themes & disfavored)
    b_score = len(b_themes & favored) - len(b_themes & disfavored)

    if a_score > b_score + 0:
        return 1, (
            f"第{qn}题 A「{a}」 vs B「{b}」——直觉上 A 更贴我的设计师气质。比较认同 A。"
        )
    if b_score > a_score + 0:
        return -1, (
            f"第{qn}题 A「{a}」 vs B「{b}」——B 侧更像我私下真实的样子。比较认同 B。"
        )

    # 情绪平稳时的居中
    return 0, (
        f"第{qn}题 A「{a}」 vs B「{b}」——两种都不太像我，也可能都像一点，看当天心情。两者居中。"
    )


