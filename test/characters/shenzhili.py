def answer_gallup(question: dict) -> Tuple[int, str]:
    a = question.get("statement_a") or ""
    b = question.get("statement_b") or ""
    hint = question.get("scenario_hint") or ""
    qn = question["question_num"]

    if any(k in a for k in ("聚会", "焦点", "受欢迎", "结识", "主动打招呼", "热络", "寒暄")):
        return -2, f"第{qn}题 A「{a}」——不喜闲聊，成为焦点更不可能。非常认同 B。"
    if any(k in a for k in ("独自", "一个人", "独处", "独立解决", "独立思考")):
        return 2, f"第{qn}题 A「{a}」——深度思考本来就要一个人。非常认同 A。"
    if any(k in b for k in ("独自", "一个人", "独处", "独立解决", "独立思考")):
        return -2, f"第{qn}题 B「{b}」——解题时我不需要旁人打扰。非常认同 B。"

    logic_a = any(k in a for k in ("分析", "逻辑", "推理", "数据", "研究", "原理", "规律", "理论", "精确"))
    logic_b = any(k in b for k in ("分析", "逻辑", "推理", "数据", "研究", "原理", "规律", "理论", "精确"))
    plan_a = any(k in a for k in ("计划", "按时", "流程", "清单", "秩序", "条理", "仔细", "准确", "完成"))
    plan_b = any(k in b for k in ("计划", "按时", "流程", "清单", "秩序", "条理", "仔细", "准确"))
    social_a = any(k in a for k in ("他人", "别人", "团队", "合作", "沟通", "倾听", "体谅", "和谐", "融入"))
    social_b = any(k in b for k in ("他人", "别人", "团队", "合作", "沟通", "倾听", "体谅", "和谐"))
    vague_a = any(k in a for k in ("经常", "总是", "灵感", "顿悟", "创新想法"))

    if logic_a and not logic_b:
        return 2, f"第{qn}题 A「{a}」——客观数据和逻辑链是我判断的基准。非常认同 A。"
    if logic_b and not logic_a:
        return -2, f"第{qn}题 B「{b}」——这更像竞赛训练里的我。非常认同 B。"

    if plan_a and not plan_b:
        return 2, f"第{qn}题 A「{a}」——坚持计划、自习表精确，果断高分。比较认同 A。"
    if plan_b and not plan_a:
        return -1, f"第{qn}题 B「{b}」——有条理做题效率更高。比较认同 B。"

    if social_a and not social_b:
        return -1, f"第{qn}题 A「{a}」——小组讨论题目可以，泛泛社交不行。比较认同 B。"
    if social_b and not social_a:
        return -1, f"第{qn}题 B「{b}」——人情世故不是我关心的。比较认同 B。"

    if vague_a:
        return 1, f"第{qn}题 A「{a}」——「经常」太模糊，潜意识推理算一点灵感吧。有点认同 A。"

    a_themes = set(question.get("theme_tags") or [])
    b_themes = set(question.get("b_side_themes") or [])
    favored = {"学习", "搜集", "思维", "分析", "专注", "战略", "审慎", "责任", "成就", "纪律"}
    disfavored = {"交往", "沟通", "和谐", "取悦", "伯乐", "统率", "积极", "包容", "个别"}

    a_score = len(a_themes & favored) - len(a_themes & disfavored)
    b_score = len(b_themes & favored) - len(b_themes & disfavored)

    if a_score > b_score:
        return 1, f"第{qn}题 A「{a}」 vs B「{b}」——更贴理科生的严谨风格。比较认同 A。"
    if b_score > a_score:
        return -1, f"第{qn}题 A「{a}」 vs B「{b}」——B 侧更符合事实。比较认同 B。"

    return 0, f"第{qn}题 A「{a}」 vs B「{b}」——定义不清，{hint[:20]}… 暂选居中。"


