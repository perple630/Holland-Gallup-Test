def answer_gallup(question: dict) -> Tuple[int, str]:
    a = question.get("statement_a") or ""
    b = question.get("statement_b") or ""
    qn = question["question_num"]

    if any(k in a for k in ("规章制度", "遵守", "规则", "流程", "按计划", "按时", "清单", "秩序", "整齐", "井井有条")):
        return -2, (
            f"第{qn}题 A「{a}」——可笑，那些破规矩都是驯化人的。非常认同 B。"
        )
    if any(k in a for k in ("耐心", "等待", "坚持完成", "仔细", "谨慎", "稳妥")):
        return -2, f"第{qn}题 A「{a}」——我没耐心陪他们玩这套。非常认同 B。"

    hands_a = any(k in a for k in ("动手", "修理", "操作", "实践", "具体", "行动", "立刻", "马上"))
    hands_b = any(k in b for k in ("动手", "修理", "操作", "实践", "具体", "行动", "立刻", "马上"))
    rebel_a = any(k in a for k in ("服从", "配合", "合群", "融入", "传统", "稳定", "可靠", "负责"))
    rebel_b = any(k in b for k in ("独立", "自由", "挑战", "冒险", "打破", "不同", "独特"))
    stage_a = any(k in a for k in ("舞台", "表演", "表达", "影响", "竞争", "赢", "第一", "领先"))
    stage_b = any(k in b for k in ("舞台", "表演", "表达", "影响", "竞争", "赢", "第一", "领先"))
    book_a = any(k in a for k in ("学习", "阅读", "研究", "分析", "理论", "数据", "数字", "统计", "逻辑"))
    book_b = any(k in b for k in ("学习", "阅读", "研究", "分析", "理论", "数据", "数字", "统计"))
    team_good = any(k in a for k in ("合作", "团队", "和谐", "包容", "理解", "倾听", "他人感受"))
    risk_a = any(k in a for k in ("风险", "冒险", "挑战", "新", "变化"))

    if hands_a and not hands_b:
        return 2, f"第{qn}题 A「{a}」——机车改装、设备修理，这就是我。非常认同 A。"
    if hands_b and not hands_a:
        return -2, f"第{qn}题 B「{b}」——动手的事我认 B。非常认同 B。"

    if rebel_a and rebel_b:
        return -2, f"第{qn}题 A「{a}」 vs B「{b}」——反抗权威是我的默认设置。非常认同 B。"
    if rebel_a and not rebel_b:
        return -2, f"第{qn}题 A「{a}」——好学生标准答案？我偏不。非常认同 B。"

    if stage_a and not stage_b:
        return 2, f"第{qn}题 A「{a}」——台上台下我都在乎存在感。非常认同 A。"
    if stage_b and not stage_a:
        return -2, f"第{qn}题 B「{b}」——摇滚现场那劲儿，站 B。非常认同 B。"

    if book_a and not any(k in b for k in ("动手", "行动", "实践", "创意", "音乐")):
        return -2, f"第{qn}题 A「{a}」——书本公式记不住，不是那块料。非常认同 B。"

    if team_good and any(k in b for k in ("直接", "独立", "自己", "竞争", "赢")):
        return -1, f"第{qn}题 A「{a}」——对师长那套假和谐没兴趣。比较认同 B。"

    if risk_a:
        return 2, f"第{qn}题 A「{a}」——翻墙、狂飙，风险我不怕。非常认同 A。"

    if any(k in a for k in ("未来", "远景", "规划", "目标")) and any(k in b for k in ("现在", "当下", "立刻")):
        return -1, f"第{qn}题：未来没明确规划，只有模糊的叛逆梦想。比较认同 B。"

    a_themes = set(question.get("theme_tags") or [])
    b_themes = set(question.get("b_side_themes") or [])
    favored = {"行动", "统率", "竞争", "追求", "理念", "积极", "自信", "完美", "前瞻"}
    disfavored = {"纪律", "专注", "统筹", "公平", "责任", "和谐", "包容", "交往", "伯乐", "审慎", "学习", "搜集"}

    a_score = len(a_themes & favored) - len(a_themes & disfavored)
    b_score = len(b_themes & favored) - len(b_themes & disfavored)

    if a_score > b_score:
        return 1, f"第{qn}题 A「{a}」 vs B「{b}」——叛逆直接，直觉偏 A。比较认同 A。"
    if b_score > a_score:
        return -1, f"第{qn}题 A「{a}」 vs B「{b}」——B 侧更像我私下真实的样子。比较认同 B。"

    return 0, f"第{qn}题 A「{a}」 vs B「{b}」——两种都不太像好学生，懒得选。两者居中。"


