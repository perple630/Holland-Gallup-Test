def answer_gallup(question: dict) -> Tuple[int, str]:
    a = question.get("statement_a") or ""
    b = question.get("statement_b") or ""
    qn = question["question_num"]

    if any(k in a for k in ("压力", "冷静", "镇定", "紧张", "焦虑", "临危")):
        return 2, (
            f"第{qn}题 A「{a}」——比赛最后几十秒要罚球，全场都看你，不冷静早输了。非常认同 A。"
        )
    if any(k in b for k in ("压力", "冷静", "镇定", "紧张", "焦虑", "临危")):
        return -2, f"第{qn}题 B「{b}」——关键球时刻我反而稳，站 B 这边。非常认同 B。"

    team_a = any(k in a for k in ("合作", "团队", "一起", "我们", "集体", "配合", "融入"))
    team_b = any(k in b for k in ("合作", "团队", "一起", "我们", "集体", "配合"))
    action_a = any(k in a for k in ("行动", "立刻", "马上", "开始", "执行", "完成", "推进", "动手"))
    action_b = any(k in b for k in ("行动", "立刻", "马上", "开始", "执行", "完成", "推进", "动手"))
    compete_a = any(k in a for k in ("竞争", "赢", "第一", "领先", "胜利", "超越", "比拼"))
    compete_b = any(k in b for k in ("竞争", "赢", "第一", "领先", "胜利", "超越"))
    alone_a = any(k in a for k in ("独自", "一个人", "独处", "独立", "孤独", "内向"))
    alone_b = any(k in b for k in ("独自", "一个人", "独处", "独立", "孤独"))
    theory_a = any(k in a for k in ("理论", "研究", "分析", "思考", "阅读", "学习", "学术", "逻辑", "抽象"))
    theory_b = any(k in b for k in ("理论", "研究", "分析", "思考", "阅读", "学术", "逻辑", "抽象"))
    positive_a = any(k in a for k in ("乐观", "积极", "热情", "活力", "兴奋", "开心"))
    social_a = any(k in a for k in ("每个人喜欢", "受欢迎", "健谈", "结识", "陌生人"))

    if team_a and not team_b:
        return 2, f"第{qn}题 A「{a}」——篮球是五个人的运动，团队协作我强项。非常认同 A。"
    if team_b and not team_a:
        return -2, f"第{qn}题 B「{b}」——场上场下都靠兄弟，认 B。非常认同 B。"

    if action_a and not action_b:
        return 2, f"第{qn}题 A「{a}」——想得少做得多，行动派。非常认同 A。"
    if action_b and not action_a:
        return -2, f"第{qn}题 B「{b}」——教练喊跑我就跑，站 B。非常认同 B。"

    if compete_a and not compete_b:
        return 2, f"第{qn}题 A「{a}」——上场就是要赢。非常认同 A。"
    if compete_b and not compete_a:
        return -2, f"第{qn}题 B「{b}」——竞争让我兴奋。非常认同 B。"

    if alone_a and not alone_b:
        return -2, f"第{qn}题 A「{a}」——我能量来自队友和观众，不爱一个人闷着。非常认同 B。"
    if alone_b and not alone_a:
        return 2, f"第{qn}题 A「{a}」——独处思考不适合我，相对更站 A。"

    if theory_a and not any(k in b for k in ("行动", "实践", "比赛", "运动", "团队")):
        return -2, f"第{qn}题 A「{a}」——复杂理论不耐受，实践出真知。非常认同 B。"
    if theory_b and not theory_a:
        return -1, f"第{qn}题 B「{b}」——书本那套我认栽，略偏 B 或反选… 比较认同 B。"

    if positive_a:
        return 2, f"第{qn}题 A「{a}」——乐观现实，情绪稳定，输球也能很快调整。非常认同 A。"

    if social_a:
        return 2, f"第{qn}题 A「{a}」——群体核心，更衣室气氛我负责。非常认同 A。"

    if any(k in a for k in ("坚持", "纪律", "计划", "按时", "目标")):
        return 1, f"第{qn}题 A「{a}」——训练纪律我能坚持，C 在训练上不低。比较认同 A。"

    a_themes = set(question.get("theme_tags") or [])
    b_themes = set(question.get("b_side_themes") or [])
    favored = {"行动", "统率", "竞争", "成就", "追求", "积极", "自信", "和谐", "交往", "伯乐", "体谅"}
    disfavored = {"思维", "学习", "搜集", "分析", "理念", "审慎", "完美", "前瞻", "个别"}

    a_score = len(a_themes & favored) - len(a_themes & disfavored)
    b_score = len(b_themes & favored) - len(b_themes & disfavored)

    if a_score > b_score:
        return 1, f"第{qn}题 A「{a}」 vs B「{b}」——直率行动派，直觉偏 A。比较认同 A。"
    if b_score > a_score:
        return -1, f"第{qn}题 A「{a}」 vs B「{b}」——B 侧更像场上的我。比较认同 B。"

    return 0, f"第{qn}题 A「{a}」 vs B「{b}」——场上场下都干脆，不纠结。两者居中。"


