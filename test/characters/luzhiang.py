def answer_gallup(question: dict) -> Tuple[int, str]:
    a = question.get("statement_a") or ""
    b = question.get("statement_b") or ""
    qn = question["question_num"]

    # 角色文档典型反应
    if any(k in a for k in ("井井有条", "整齐", "按计划", "规章制度", "流程")):
        return -2, (
            f"第{qn}题 A「{a}」——井井有条会杀死我。我的桌面和生活是灾难，但作品可以很棒。非常认同 B。"
        )
    if "满意" in a and ("工作" in a or "完成" in a):
        return -1, (
            f"第{qn}题 A「{a}」——没灵感时我对作品满意不起来；有灵感时又不满意到想推翻重来。比较认同 B。"
        )

    creative_a = any(k in a for k in ("创意", "艺术", "设计", "美", "想象", "独特", "新颖", "故事", "表达"))
    creative_b = any(k in b for k in ("创意", "艺术", "设计", "美", "想象", "独特", "新颖", "故事", "表达"))
    rigid_a = any(k in a for k in ("规则", "流程", "计划", "纪律", "按时", "清单", "规范", "秩序", "一致"))
    rigid_b = any(k in b for k in ("规则", "流程", "计划", "纪律", "按时", "清单", "规范", "秩序"))
    team_a = any(k in a for k in ("合作", "团队", "一起", "协调", "融入", "合群"))
    team_b = any(k in b for k in ("合作", "团队", "一起", "个人", "独立", "独自"))
    social_a = any(k in a for k in ("每个人喜欢", "受欢迎", "讨人喜欢", "健谈", "陌生人"))
    math_a = any(k in a for k in ("数据", "数字", "统计", "逻辑", "分析")) and "创意" not in a
    rebel_commercial = any(k in a for k in ("商业", "营销", "销售", "KPI", "第一", "竞争"))

    if creative_a and not creative_b:
        return 2, f"第{qn}题 A「{a}」——这就是我作为视觉传达学生的呼吸方式。非常认同 A。"
    if creative_b and not creative_a:
        return -2, f"第{qn}题 B「{b}」——灵魂站 B 这边。非常认同 B。"

    if rigid_a and not rigid_b:
        return -2, f"第{qn}题 A「{a}」——艺术家不该被条条框框驯化。非常认同 B。"
    if rigid_b and not rigid_a:
        return 2, f"第{qn}题 A「{a}」——自由对我更重要，相对 B 的 rigid 我站 A。"

    if team_a and "独立" in b or "个人" in b or "独自" in b:
        return -2, f"第{qn}题 A「{a}」 vs B「{b}」——团队协作很糟，我更适合一个人出稿。非常认同 B。"
    if team_b and "合作" in a:
        return -1, f"第{qn}题：合作题让我本能想逃，略偏 B 侧独立描述。"

    if social_a:
        return -2, f"第{qn}题 A「{a}」——我不需要所有人喜欢我，那太假了。非常认同 B。"

    if rebel_commercial:
        return -1, f"第{qn}题 A「{a}」——对商业限制充满嘲讽，但得生存。比较认同 B 或居中… 比较认同 B。"

    if math_a and not any(k in b for k in ("创意", "艺术", "设计")):
        return -1, f"第{qn}题 A「{a}」——数理逻辑毫无兴趣。比较认同 B。"

    perfection_a = any(k in a for k in ("完美", "改进", "最高标准", "尽善尽美"))
    if perfection_a:
        return 2, f"第{qn}题 A「{a}」——我对作品永远不满意，但这题说的是追求卓越，我认。非常认同 A。"

    if any(k in a for k in ("未来", "远景", "趋势")) and "历史" in b:
        return 1, f"第{qn}题：更想聊视觉趋势和未来叙事。比较认同 A。"

    a_themes = set(question.get("theme_tags") or [])
    b_themes = set(question.get("b_side_themes") or [])
    favored = {"理念", "完美", "个别", "前瞻", "思维", "搜集", "战略", "积极"}
    disfavored = {"纪律", "专注", "统筹", "公平", "责任", "和谐", "包容", "交往", "伯乐", "审慎"}

    a_score = len(a_themes & favored) - len(a_themes & disfavored)
    b_score = len(b_themes & favored) - len(b_themes & disfavored)

    if a_score > b_score:
        return 1, f"第{qn}题 A「{a}」 vs B「{b}」——直觉偏 A，符合我的艺术家气质。比较认同 A。"
    if b_score > a_score:
        return -1, f"第{qn}题 A「{a}」 vs B「{b}」——B 侧更像我私下真实的样子。比较认同 B。"

    return 0, f"第{qn}题 A「{a}」 vs B「{b}」——两种都不太像我，看当天情绪。两者居中。"


